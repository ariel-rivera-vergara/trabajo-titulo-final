    private void btn_agregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_agregarMouseClicked
         
        prov.setCod_prov(trim(txt_cod_prov.getText()));
        prov.setNom_prov(trim(txt_nom_prov.getText()));
        
    if  (trim(prov.getCod_prov()).equals("") || trim(prov.getNom_prov()).equals("")){
              JOptionPane.showMessageDialog(null, "indique codigo no se puede agregar un proveedor sin codigo o sin nombre");
              
          }else{
              if(JOptionPane.showConfirmDialog(null, "Desea agregar el proveedor de codigo  " + prov.getCod_prov(), "agregar proveedor",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null) == JOptionPane.OK_OPTION) {     
                      prov.setCod_ciudad(trim(txt_cod_ciudad_prov.getText()));
                      prov.setFono_prov(trim(txt_fono_prov.getText()));
                      prov.setDirec_prov(trim(txt_direc_prov.getText()));
                      prov.setDesc_prov(trim(txt_desc_prov.getText()));
                                qp.agregarproveedor(prov); 
                                    actualizar();
                                     Limpiar();
    }                                        
               Limpiar();
    
    }
        
    }//GEN-LAST:event_btn_agregarMouseClicked


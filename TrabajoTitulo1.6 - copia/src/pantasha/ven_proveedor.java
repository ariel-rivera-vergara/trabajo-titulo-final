    private void btn_modificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_modificarMouseClicked
 
       prov.setCod_prov(trim(txt_cod_prov.getText()));
        prov.setNom_prov(trim(txt_nom_prov.getText()));
        
    if  (trim(prov.getCod_prov()).equals("") || trim(prov.getNom_prov()).equals("")){
              JOptionPane.showMessageDialog(null, "indique codigo no se puede agregar un proveedor sin codigo o sin nombre");
              
          }else{
              if (JOptionPane.showConfirmDialog(null, "Desea modificar el proveedor con codigo  " + prov.getCod_prov(), "modificar proveedor",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null) == JOptionPane.OK_OPTION) {  
                      //validar si la ciudad del proveedor existe  
                  prov.setCod_ciudad(trim(txt_cod_ciudad_prov.getText()));
                  prov.setFono_prov(trim(txt_fono_prov.getText()));
                  prov.setDirec_prov(trim(txt_direc_prov.getText()));
                  prov.setDesc_prov(trim(txt_desc_prov.getText()));
                  
                    qp.modificarproveedor(prov); 
                    actualizar();
                    Limpiar();
                  }
           Limpiar(); 
          }        
        
        
    }//GEN-LAST:event_btn_modificarMouseClicked

    private void btn_eliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_eliminarMouseClicked
  
                 prov.setCod_prov(trim(txt_cod_prov.getText()));
          if  (prov.getCod_prov().equals("")){
              JOptionPane.showMessageDialog(null, "Seleccione o indique codigo del proveedor a eliminar");
              
          }else{
              if (JOptionPane.showConfirmDialog(null, "Desea eliminar el proveedor de codigo  " + prov.getCod_prov(), "Eliminar proveedor",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null) == JOptionPane.OK_OPTION) {
                qp.eliminarproveedor(prov.getCod_prov());
                tbl_proveedores.removeAll();
                
                actualizar();
                Limpiar();
              }
               Limpiar();
          } 
        
        
    }//GEN-LAST:event_btn_eliminarMouseClicked


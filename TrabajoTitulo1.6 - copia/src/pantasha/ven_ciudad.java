    private void btn_eliminar_ciudadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminar_ciudadActionPerformed
             ci.setCod_ciudad(trim(txt_cod_ciudad.getText()));
          if  (ci.getCod_ciudad().equals("")){
              JOptionPane.showMessageDialog(null, "Seleccione o indique codigo del producto a eliminar");
              
          }else{
              if (JOptionPane.showConfirmDialog(null, "Desea eliminar la ciudad con codigo  " + ci.getCod_ciudad(), "Eliminar ciudad",
                    JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE, null) == JOptionPane.OK_OPTION) {
                qp.eliminarciudad(ci.getCod_ciudad());
                tbl_ciudad.removeAll();
                
                actualizar();
                Limpiar();
              }
               Limpiar();
          } 
    }//GEN-LAST:event_btn_eliminar_ciudadActionPerformed


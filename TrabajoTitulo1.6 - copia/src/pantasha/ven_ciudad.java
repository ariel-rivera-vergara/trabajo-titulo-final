    private void btn_agregar_ciudadMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_agregar_ciudadMouseClicked
        ci.setCod_ciudad(trim(txt_cod_ciudad.getText()));
        ci.setNom_ciudad(trim(txt_nom_ciudad.getText()));

        if (trim(ci.getCod_ciudad()).equals("") || trim(ci.getNom_ciudad()).equals("")) {
            JOptionPane.showMessageDialog(null, "indique codigo no se puede agregar una ciudad sin codigo o sin nombre");

        } else {

            qp.agregarciudad(ci);

            actualizar();
            Limpiar();
        }

        Limpiar();
    
    }//GEN-LAST:event_btn_agregar_ciudadMouseClicked



package clases;


public class Proveedores {
    private String cod_prov;
    private String cod_ciudad;
    private String nom_prov;
    private String fono_prov;
    private String direc_prov;
    private String desc_prov;

    public String getCod_prov() {
        return cod_prov;
    }

    public void setCod_prov(String cod_prov) {
        this.cod_prov = cod_prov;
    }

    public String getCod_ciudad() {
        return cod_ciudad;
    }

    public void setCod_ciudad(String cod_ciudad) {
        this.cod_ciudad = cod_ciudad;
    }

    public String getNom_prov() {
        return nom_prov;
    }

    public void setNom_prov(String nom_prov) {
        this.nom_prov = nom_prov;
    }

    public String getFono_prov() {
        return fono_prov;
    }

    public void setFono_prov(String fono_prov) {
        this.fono_prov = fono_prov;
    }

    public String getDirec_prov() {
        return direc_prov;
    }

    public void setDirec_prov(String direc_prov) {
        this.direc_prov = direc_prov;
    }

    public String getDesc_prov() {
        return desc_prov;
    }

    public void setDesc_prov(String desc_prov) {
        this.desc_prov = desc_prov;
    }
    
    
       
    
}

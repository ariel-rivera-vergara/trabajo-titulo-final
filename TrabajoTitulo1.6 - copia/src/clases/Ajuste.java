
package clases;

import java.util.Date;


public class Ajuste {
   private int cod_ajuste;
   private String cod_prod;
   private String tipo_ajuste;
   private int cant_ajuste;
   private String fecha_ajuste; 
   private String comentario;

    public int getCod_ajuste() {
        return cod_ajuste;
    }

    public void setCod_ajuste(int cod_ajuste) {
        this.cod_ajuste = cod_ajuste;
    }

    public String getCod_prod() {
        return cod_prod;
    }

    public void setCod_prod(String cod_prod) {
        this.cod_prod = cod_prod;
    }

    public String getTipo_ajuste() {
        return tipo_ajuste;
    }

    public void setTipo_ajuste(String tipo_ajuste) {
        this.tipo_ajuste = tipo_ajuste;
    }

    public int getCant_ajuste() {
        return cant_ajuste;
    }

    public void setCant_ajuste(int cant_ajuste) {
        this.cant_ajuste = cant_ajuste;
    }

    public String getFecha_ajuste() {
        return fecha_ajuste;
    }

    public void setFecha_ajuste(String fecha_ajuste) {
        this.fecha_ajuste = fecha_ajuste;
    }

    public String getComentario() {
        return comentario;
    }

    public void setComentario(String comentario) {
        this.comentario = comentario;
    }

   
  
   
   
}
